// Comparison Operators
// return true or false.
const a = 5;
const b = 10;

// Greater than
console.log(a > b);

// Greater than or equal to
console.log(a >= b);

// Less than
console.log(a < b);

// Less than or equal to
console.log(a <= b);

// Equality Operators
console.log(a == b);

// Not Equal
console.log(a != b);

// Strict Equality
console.log(a === b);

// Strict Inequality
console.log(a !== b);